#include "functions.h"

int main()
{
    LRUCache* cache = createLRUCache(CAPACITY);

    while(1)
    {
        int choice = menu();

        if (choice == 0) exit(0);

        else if
                (choice == 1) ;

        else if
                (choice == 2) getDomain(cache);

        else if
                (choice == 3) ;

        else if
                (choice == 4) ;

        printf("\n");
    }
}
